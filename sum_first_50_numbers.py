#PROGRAM: Sum of first 50 Numbers (0 to 49)
#Description: Uses a for loop to calculate total sum.

total = 0 #Initialize with a varibale with sum 0
for i in range(50): # i counts from 0 to 49
    total = total + i # Adds current value to the total sum present at that time

print(total) # Displays the final calculated sum
